package week03;

public class RelationalExample {
    public static void main(String[] args) {

        // Relational Operators
        // > greater than sign
        // < less than sign
        // >= greater equal
        // <= less than equal
        // == equal
        // != not equal
        int i1, i2;
        i1 = 100;
        i2 = 20;

        boolean a = i1 > i2;
        System.out.println("Is the first number bigger than second one: " + a);

        /*
        & And Operator, && Bitwise And Operator
        | Or operator , || Bitwise or Operator
         */
    }
}
